// Regenerate the docs/storybook gallery modules (09/10) from the components'
// own co-located *.dev.js files, replacing the old hand-maintained ports.
//
// For each gallery it bundles a small aggregator entry with Bun.build:
//   - "tutuca" / "tutuca/components" stay external (packages: "external") — the
//     published page resolves them via its import map, `bun run storybook` via
//     the local dist;
//   - every relative import of a component module (./json.js, ../data/….js)
//     is externalized to the single bare "tutuca/components" specifier, whose
//     barrel re-exports all of them;
//   - relative *.dev.js imports (test-inspector.dev.js → component-inspector.
//     dev.js) stay bundled, since dev modules are not part of the barrel.
// The aggregator tags every section with the gallery's sidebar `group` and
// composes the sources' getTests.
import { mkdtempSync, rmSync, writeFileSync } from "node:fs";
import { tmpdir } from "node:os";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const ROOT = resolve(dirname(fileURLToPath(import.meta.url)), "..");

const GALLERIES = [
  {
    out: "docs/storybook/09-data-inspectors.dev.js",
    group: "Data Inspectors",
    title: "09 — DATA INSPECTORS",
    sources: [
      "src/components/data/json.dev.js",
      "src/components/data/data.dev.js",
      "src/components/data/immutable-inspector.dev.js",
      "src/components/data/json-schema.dev.js",
    ],
  },
  {
    out: "docs/storybook/10-tutuca-inspectors.dev.js",
    group: "Tutuca Inspectors",
    title: "10 — TUTUCA INSPECTORS",
    sources: [
      "src/components/tutuca/component-inspector.dev.js",
      "src/components/tutuca/instance-inspector.dev.js",
      "src/components/tutuca/lint-inspector.dev.js",
      "src/components/tutuca/test-inspector.dev.js",
    ],
  },
];

// Externalize relative imports of component modules to the "tutuca/components"
// barrel; keep relative *.dev.js imports internal so they get bundled.
const externalizeComponents = {
  name: "externalize-components",
  setup(build) {
    build.onResolve({ filter: /^\.\.?\/.*(?<!\.dev)\.js$/ }, () => ({
      path: "tutuca/components",
      external: true,
    }));
  },
};

function aggregatorSource({ group, sources }) {
  const imports = sources
    .map((rel, i) => `import * as m${i} from ${JSON.stringify(resolve(ROOT, rel))};`)
    .join("\n");
  const names = sources.map((_, i) => `m${i}`).join(", ");
  return `${imports}
export { getComponents } from "tutuca/components";
const MODULES = [${names}];
const GROUP = ${JSON.stringify(group)};
export function getExamples() {
  return MODULES.flatMap((m) => {
    const raw = m.getExamples();
    return (Array.isArray(raw) ? raw : [raw]).map((s) => ({ group: GROUP, ...s }));
  });
}
export function getTests(ctx) {
  for (const m of MODULES) if (typeof m.getTests === "function") m.getTests(ctx);
}
`;
}

function banner(gallery) {
  const sourceList = gallery.sources.map((s) => `//   ${s}`).join("\n");
  return `// ${gallery.title} — GENERATED by scripts/sync-docs-storybook.js, DO NOT EDIT.
//
// Aggregates the galleries of the real in-repo inspector components from:
${sourceList}
// with their relative component imports rewritten to the bare
// "tutuca/components" specifier (resolved via the import map in index.html on
// the published page, or the freshly built local dist under \`bun run
// storybook\`). Each section is tagged with group ${JSON.stringify(gallery.group)} so they
// cluster under one collapsible sidebar header.
`;
}

const tmp = mkdtempSync(join(tmpdir(), "tutuca-gallery-"));
try {
  for (const gallery of GALLERIES) {
    const entry = join(tmp, gallery.out.split("/").at(-1));
    writeFileSync(entry, aggregatorSource(gallery));
    const result = await Bun.build({
      entrypoints: [entry],
      format: "esm",
      packages: "external",
      plugins: [externalizeComponents],
    });
    if (!result.success) {
      for (const log of result.logs) console.error(log);
      process.exit(1);
    }
    // Bun keeps the ORIGINAL specifier for external imports (same quirk
    // scripts/dist-ext.js works around), so rewrite the relative component
    // specifiers to the bare package export after the build.
    const text = (await result.outputs[0].text()).replaceAll(
      /(["'])(?:\.\.?\/)+(?:[\w-]+\/)*(?!.*\.dev\.js)[\w-]+\.js\1/g,
      '"tutuca/components"',
    );
    const outPath = resolve(ROOT, gallery.out);
    writeFileSync(outPath, banner(gallery) + text);
    console.log(`wrote ${gallery.out}`);
  }
} finally {
  rmSync(tmp, { recursive: true, force: true });
}
