#!/usr/bin/env bun
import { cpSync, mkdirSync, rmSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const here = dirname(fileURLToPath(import.meta.url));
const repo = resolve(here, "..");
const srcDir = resolve(repo, "docs/llm");
const outDir = resolve(repo, "skill/tutuca");

const SKILL_FRONTMATTER = `---
name: tutuca
description: Use when authoring or reviewing tutuca modules — \`component({...})\` definitions, \`html\\\`...\\\`\` views, \`@\`-directives, \`input\` / \`bubble\` / \`receive\` / \`response\` / \`alter\` handlers, macros, or \`getTests\` exports — or when running the \`tutuca\` CLI (\`lint\` / \`test\` / \`render\` / \`docs\`). Covers the post-edit \`tutuca <module> lint\` → \`test\` → \`render --title "<example>"\` verification recipe.
---

<!-- This file is generated by scripts/build-skill.js from docs/llm/. Do not edit. -->

`;

const SKILL_BODY = `# Tutuca

Tutuca is an immutable-state SPA framework. See [core.md](./core.md)
for the framework primer and the post-edit verification recipe
(\`tutuca <module> lint\` → \`test\` → \`render --title "<example>"\`).

## Companion skills

When authoring tutuca code, also load these if available:

- **immutable-js** — every \`immutable\` export is reachable through
  \`tutuca\` (\`List\`, \`IMap\`, \`OMap\`, \`ISet\`, \`Record\`, \`Seq\`,
  \`fromJS\`, \`is\`, …). Reach for it whenever the work touches state
  values.
- **margaui** — the Tailwind v4 / daisyUI-compatible class library.
  Reach for it when the project uses MargaUI / Tailwind class lists in
  \`class=\` / \`:class=\`.

## Routing

| Task                                                                                           | File                            |
| ---------------------------------------------------------------------------------------------- | ------------------------------- |
| Authoring \`component({...})\`, \`html\\\`...\\\`\` views, macros, fields, events, lists, styles | [core.md](./core.md)           |
| CLI commands, flags, exit codes, full linter rule list                                         | [cli.md](./cli.md)             |
| Drag & drop, dynamic bindings (\`*x\`), pseudo-\`x\`, custom seq types, Tailwind/MargaUI | [advanced.md](./advanced.md)   |
| Authoring tests — \`getTests\` shape, calling methods/input/receive/bubble/response/alter handlers, designing handlers for testability | [testing.md](./testing.md) |

Read \`core.md\` first. Reach for \`cli.md\`, \`advanced.md\`, or
\`testing.md\` only when the task touches them — all three are
referenced inline from \`core.md\` so you'll be pointed there when
relevant.
`;

function build() {
  rmSync(outDir, { recursive: true, force: true });
  mkdirSync(outDir, { recursive: true });

  for (const name of ["core", "cli", "advanced", "testing"]) {
    cpSync(resolve(srcDir, `${name}.md`), resolve(outDir, `${name}.md`));
  }

  writeFileSync(resolve(outDir, "SKILL.md"), SKILL_FRONTMATTER + SKILL_BODY);

  process.stdout.write(`built skill/ from docs/llm/ → ${outDir}\n`);
}

build();
